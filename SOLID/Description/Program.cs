/* Single Responsibility Principle (SRP) (Tek Sorumluluk İlkesi) */
// Bu prensip, Bir sınınıfın veya modülün sadece bir sorumluluğu olmasını belirtir. Yani, bir sınıfın yalnızca tek bir iş veya görevi olmalı ve bu göreve dair tüm işlevri yerine getirmelidir.

/* Open Closed Principle (OCP) (Açık Kaplı Prensip) */
// Yazılım bileşenleri (Sınfılar, modüller vb.) genişletilmeye açık, ancak değiştirilmeye kapalı olmalıdır. Yani var olan bir sınıfın davranışı değiştirilmeden yeni işlevsellikler eklenebilir olmalıdır.

/* Liskov Substitution Principle (LSP) (Liskov'un Yer Değiştirme Prensibi) */
// Türemiş sınfılar, temel sınfıların tüm özelliklerini ve işlevlerini değiştirmeden kullanılması ve temel sınıfın yerini alabilmelidir. Bu bir alt sınıfın üst sınıfın tüm davranışları doğru bir şekilde uygulaması gerektiği anlamına gelir.

/* Interface Segregation Principle (ISP) (Arayüz Ayrımı Prensibi) */
// Bir sınıf, kullanmadığı metodları içeren geniş kapsamlı bir arayüze(interface) bağlı olmamalıdır. Bunun yereine, spefik görevleri yerine getiren daha küçük ve odaklanmış arayüzlere sahip olmalıdır.

/* Dependency Inversion Principle (DIP) (Bağımlıkların Ters Çevrilmesi Prensibi) */
// Türemiş sınıflar, temel sınfıların tüm özelliklerini ve işlevlerini değiştirmeden kullanılması ve temel sınıfın yerini alabilmelidir. Bu bir alt sınıfın üst sınıfın tüm davranışları doğru bir şekilde uygulaması gerektiği anlamına gelir.